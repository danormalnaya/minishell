#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	t_list	*tmp;
	int		count;

	if (!lst)
		return (0);
	count = 1;
	tmp = lst;
	while (tmp->next)
	{
		tmp = tmp->next;
		count++;
	}
	return (count);
}
